package com.rs.eis.request;


public class PrefixVo{
private int id;
	private String codeVo;

	private String valueVo;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCodeVo() {
		return codeVo;
	}

	public void setCodeVo(String codeVo) {
		this.codeVo = codeVo;
	}

	public String getValueVo() {
		return valueVo;
	}

	public void setValueVo(String valueVo) {
		this.valueVo = valueVo;
	}

}
